
<template>

<div class="wrapper">
  <div class="cart-products">
    <div class="cart-product" v-for="product in cart" :key="product.id">
      <div class="info">
        <h1>{{product.name}}</h1>
        <p>{{product.country}}</p>
      </div>
      <div class="image">
        <img :src="'/images/products/'+product.image">
      </div>
      <div class="price">
        <h2>{{product.price}}</h2>
        <button class="auto" @click="removeItem(product)">Remove From Cart</button>
      </div>
    </div>
  </div>
  <h2>{{cartEmpty}}</h2>
</div>
</template>

<script>


export default {

  name: 'Cart',
  components: {

  },
  data() {
    return {
    }
  },
  computed: {
    cart() {
      return this.$root.$data.cart;
    },
    cartEmpty() {
      if (this.$root.$data.cart == 0) {
        return "Cart Empty!";
      }
      else {
        return "";
      }
    }
  },
  methods: {
    removeItem(product) {
      for (let i = 0; i < this.$root.$data.cart.length; ++i) {
        if (product == this.$root.$data.cart.[i]) {
          this.$root.$data.cart.splice(i,1);
        }
      }
    }
  }
}

</script>

